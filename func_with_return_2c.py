
def concat_list(list1, list2, list3) -> list:
    """
    Concatenates three lists into one.

    Parameters:
    list1 (list of int): First list of integers.
    list2 (list of int): Second list of integers.
    list3 (list of int): Third list of integers.

    Returns:
    list of int: A single list containing all elements from the three lists.
    """
    return list1 + list2 + list3;
